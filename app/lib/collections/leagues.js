Leagues = new Mongo.Collection('leagues');


if (Meteor.isServer) {

  Meteor.publish('leaguesColl', function(){
    return Leagues.find();
  });

  Leagues.allow({
    insert: function (userId, doc) {
      return false;
    },

    update: function (userId, doc, fieldNames, modifier) {
      return false;
    },

    remove: function (userId, doc) {
      return false;
    }
  });

  Leagues.deny({
    insert: function (userId, doc) {
      return true;
    },

    update: function (userId, doc, fieldNames, modifier) {
      return true;
    },

    remove: function (userId, doc) {
      return true;
    }
  });
}
